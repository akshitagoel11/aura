/**
 * n8n Webhook Integration
 * Handles communication with n8n workflows for AI intent processing
 */

export interface N8nResponse {
  intent: string;
  action_payload: any;
  final_message: string;
  is_preview: boolean;
  requires_confirmation: boolean;
  success?: boolean;
  error?: string;
}

export interface WebhookPayload {
  task: string;
  user_id?: string;
  intent_type?: string;
  preview_mode?: boolean;
  execute?: boolean;
}

export interface WebhookResponse {
  status: 'success' | 'error';
  preview?: string;
  executionId?: string;
  message?: string;
  error?: string;
  timestamp: string;
}

/**
 * Call n8n webhook for AI task processing
 * Uses the specified webhook URL from the requirements
 */
export async function callN8nWebhook(payload: WebhookPayload): Promise<N8nResponse | null> {
  const webhookUrl = 'https://n8n.mediajade.com/webhook/ai-task-agent';

  try {
    console.log('[n8n] Calling webhook for task:', payload.task);

    const response = await fetch(webhookUrl, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify(payload),
    });

    if (!response.ok) {
      const error = await response.text();
      console.error('[n8n] Webhook error:', response.status, error);
      return null;
    }

    const data = (await response.json()) as N8nResponse;
    console.log('[n8n] Webhook response received');

    return data;
  } catch (error) {
    console.error('[n8n] Webhook call failed:', error);
    return null;
  }
}

/**
 * Generate intent preview (with fallback mock implementation)
 */
export async function generateIntentPreview(
  userId: string,
  intent: string,
  intentType: string
): Promise<string | null> {
  try {
    const response = await callN8nWebhook({
      task: intent,
      user_id: userId,
      intent_type: intentType,
      preview_mode: true,
    });

    if (response && response.final_message) {
      return response.final_message;
    }
  } catch (error) {
    console.warn('[n8n] Webhook failed, using mock preview:', error);
  }

  // Fallback mock preview
  return generateMockPreview(intent, intentType);
}

