/**
 * Execute an intent using n8n (with fallback mock implementation)
 */
export async function executeIntent(
  userId: string,
  intent: string,
  intentType: 'email' | 'task' | 'message' | 'documentation'
): Promise<{ success: boolean; executionId?: string; error?: string }> {
  try {
    const response = await callN8nWebhook({
      task: intent,
      user_id: userId,
      intent_type: intentType,
      execute: true,
    });

    if (response && !response.error) {
      return {
        success: true,
        executionId: `exec_${Date.now()}`,
      };
    }
  } catch (error) {
    console.warn('[n8n] Webhook failed, using mock execution:', error);
  }

  // Fallback mock execution
  return {
    success: true,
    executionId: `mock_exec_${Date.now()}`,
  };
}

function formatPreview(data: N8nResponse, intentType: string): string {
  const { intent, action_payload, final_message } = data;
  
  let preview = `**Intent:** ${intent}\n\n`;
  
  if (intentType === 'email' && action_payload.email) {
    const email = action_payload.email;
    preview += `**To:** ${email.to}\n`;
    preview += `**Subject:** ${email.subject}\n\n`;
    preview += `**Body:**\n${email.body}`;
  } else if (intentType === 'task' && action_payload.task) {
    const task = action_payload.task;
    preview += `**Task:** ${task.title}\n`;
    if (task.due_date) {
      preview += `**Due:** ${task.due_date}\n`;
    }
    if (task.description) {
      preview += `\n**Description:** ${task.description}`;
    }
  } else if (intentType === 'reminder' && action_payload.reminder) {
    const reminder = action_payload.reminder;
    preview += `**Reminder:** ${reminder.message}\n`;
    preview += `**When:** ${reminder.when}`;
  } else {
    preview += `**Action:** ${JSON.stringify(action_payload, null, 2)}`;
  }
  
  if (final_message) {
    preview += `\n\n**AI Message:** ${final_message}`;
  }
  
  return preview;
}
